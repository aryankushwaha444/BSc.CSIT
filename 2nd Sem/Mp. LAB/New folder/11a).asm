.MODEL SMALL 
.STACK 100H 
.DATA 
STR1 DB 'ENTER A STRING:','$' 
STR2 DB 30 DUP ('$') 
.CODE 
MAIN PROC 
MOV AX, @DATA 
MOV DS, AX 
LEA DX, STR1 
MOV AH, 09H 
INT 21H 
 
LEA DX, STR2 
MOV AH,0AH
INT 21H 
LEA SI, STR2 
MOV CX, [SI+1] 
MOV CH, 00H 
PRINT: MOV DL, [SI+2] 
SUB DL, 20H 
MOV AH, 02H 
INT 21H 
INC SI 
LOOP PRINT 
MOV AH, 4CH 
INT 21H 
MAIN ENDP 
END MAIN